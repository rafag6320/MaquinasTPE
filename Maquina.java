package TPEspecial;
;

public class Maquina {
  	private int piezas;
  	private String nombre;
	
	public Maquina(int piezas, String nombre) {
		this.piezas = pieza;
		this.nombre = nombre;
	}
	
	public String getNombre() {
		return this.nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public void setPiezas(int piezas) {
		this.piezas = piezas;
	}

	public int getPiezas() {
		return this.piezas;
	}

}
